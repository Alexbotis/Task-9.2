package botis.itstep.by;

public class Recipient {

    private String name;
    private String adress;

    public Recipient (String name,String adress){

        this.name=name;
        this.adress=adress;

    }

    public String getName() {
        return name;
    }

    public String getAdress() {
        return adress;
    }
}




